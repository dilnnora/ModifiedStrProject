package LazyEditor;



public class modifiedStr {
	
	private int flag; 
	private String sentence; 
	
	
    public modifiedStr(){
    	flag=0;
    	sentence="";
    }
	
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}
	public String getSentence() {
		return sentence;
	}
	public void setSentence(String sentence) {
		this.sentence = sentence;
	}
	

}
